/* Exercício 15: Função de Verificação de Número Par - Crie uma função ehPar(numero) que retorne true se o número for par e false se for ímpar.*/

function ehPar(numero){
    return numero % 2 ===0;    
}

console.log(ehPar(4))

console.log(ehPar(7))

console.log(ehPar(-9))

console.log(ehPar(-8))

console.log(ehPar(-13))


